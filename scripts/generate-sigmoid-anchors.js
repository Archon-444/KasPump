/**
 * Off-chain generator for the on-chain sigmoid bonding curve anchor table.
 *
 * Plan reference: §1 of /root/.claude/plans/alright-here-s-the-glowing-wind.md.
 *
 * Sigmoid:  P(s) = a / (1 + exp(-k · (s - s0)))
 *
 * Units throughout the generator:
 *   - s, s0:        whole tokens (Number; the contract scales by PRECISION=1e18
 *                   to get token-wei before storing)
 *   - a:            wei per 1 full token (Number → Solidity uint256)
 *   - k:            real-valued, dimensionless when multiplied by (s - s0)
 *   - integral I(s): wei (Number → Solidity uint256). Integral of P(s) over
 *                    whole-token space matches the contract's cost units when
 *                    the contract treats supply in token-wei and divides by
 *                    PRECISION at the right points (the library does the
 *                    interpolation in token-wei space directly, so anchor
 *                    supplies are stored in token-wei).
 *
 * Parameter choice (calibrated for ~3 ETH raise at graduation, similar to
 * Pump.fun's ~$15k FDV target with ETH at ~$5k):
 *   TOTAL_SUPPLY        = 1,000,000,000 tokens (1B)
 *   GRADUATION_THRESHOLD= 800,000,000 tokens (80% of supply)
 *   s0 (midpoint)       = 400,000,000 tokens (50% of threshold)
 *   a (asymptote price) = 7.5 gwei per full token
 *   k                   = chosen so that |k · (s - s0)| ≈ 3 at the endpoints
 *                         of [0, GRADUATION_THRESHOLD] — sigmoid is ~95%
 *                         saturated at the upper end and ~5% at the lower.
 *
 * Anchor schedule: fixed non-uniform spacing biased toward the high-curvature
 * midpoint (4% / 3% / 4% strides across three regions). 31 anchors total.
 *
 * Usage:
 *   node scripts/generate-sigmoid-anchors.js > /tmp/anchors.txt
 *   # paste the printed Solidity arrays into BondingCurveMath.sol
 */

const TOTAL_SUPPLY_TOKENS = 1_000_000_000;       // 1B tokens
const GRADUATION_THRESHOLD_TOKENS = TOTAL_SUPPLY_TOKENS * 0.8;
const s0 = GRADUATION_THRESHOLD_TOKENS / 2;       // 400M tokens
const a = 7.5e9;                                   // 7.5 gwei per token (wei)
const k = 3 / s0;                                  // |k·(s-s0)| = 3 at s = 0 and s = 2·s0

// Anchor supplies as % of GRADUATION_THRESHOLD. Strictly increasing, no dups.
// 6 low-tail (4% stride), 19 midpoint (3% stride), 6 high-tail (4% stride).
const ANCHOR_PCTS = [
  0, 4, 8, 12, 16, 20,
  23, 26, 29, 32, 35, 38, 41, 44, 47,
  50, 53, 56, 59, 62, 65, 68, 71, 74, 77,
  80, 84, 88, 92, 96, 100,
];
if (ANCHOR_PCTS.length !== 31) {
  throw new Error(`expected 31 anchors, got ${ANCHOR_PCTS.length}`);
}

function sigmoid(sTokens) {
  return a / (1 + Math.exp(-k * (sTokens - s0)));
}

// Cumulative integral of P(s) ds from 0 to targetTokens. Composite midpoint
// rule with 200,000 panels — well below the 0.1-0.3% target accuracy floor
// for any anchor in the table (sigmoid is smooth, midpoint converges fast).
function integralFromZero(targetTokens) {
  if (targetTokens === 0) return 0;
  const N = 200_000;
  const dx = targetTokens / N;
  let sum = 0;
  for (let i = 0; i < N; i++) {
    sum += sigmoid((i + 0.5) * dx);
  }
  return sum * dx;
}

const PRECISION = 10n ** 18n;

// Build the anchor table.
const rows = ANCHOR_PCTS.map((pct) => {
  const supplyTokens = (GRADUATION_THRESHOLD_TOKENS * pct) / 100;
  const priceWei = sigmoid(supplyTokens);
  const integralWei = integralFromZero(supplyTokens);
  return {
    pct,
    supplyTokenWei: BigInt(Math.round(supplyTokens)) * PRECISION,
    priceWei: BigInt(Math.round(priceWei)),
    integralWei: BigInt(Math.round(integralWei)),
  };
});

// Output: Solidity-ready uint256[31] arrays + a quick metrics block.
function fmtArray(name, getter) {
  const lines = rows.map((r, i) => {
    const v = getter(r).toString();
    const sep = i < rows.length - 1 ? ',' : ' ';
    const pad = ' '.repeat(Math.max(0, 30 - v.length));
    return `        ${v}${sep}${pad}// idx ${i.toString().padStart(2)} — ${r.pct}% of threshold`;
  });
  return `    uint256[31] internal constant ${name} = [\n${lines.join('\n')}\n    ];`;
}

console.log('// Generated by scripts/generate-sigmoid-anchors.js — do not edit by hand.');
console.log(`// Sigmoid params: a=${a} wei, s0=${s0} tokens, k=${k} (|k·s0|=${k * s0}).`);
console.log('// Supply values are token-wei (= tokens · 1e18); price is wei per full');
console.log('// token; integral is cumulative cost in wei from supply 0 to anchor.');
console.log();
console.log(fmtArray('ANCHORS_SUPPLY', (r) => r.supplyTokenWei));
console.log();
console.log(fmtArray('ANCHORS_PRICE', (r) => r.priceWei));
console.log();
console.log(fmtArray('ANCHORS_INTEGRAL', (r) => r.integralWei));
console.log();

// Sanity printouts.
const lastRow = rows[rows.length - 1];
console.log('// Sanity:');
console.log(`//   spot price at supply 0          = ${rows[0].priceWei} wei (${Number(rows[0].priceWei) / 1e9} gwei)`);
